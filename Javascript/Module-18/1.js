// Comparition 
```js
//  5 ki 10 ar boro?   nah "False"
console.log( 5 > 10);

//  5 ki 10 ar soto?   yes "true"
console.log( 5 < 10);

//  5 ki 10 ar soman?   no "false"
console.log( 5 == 10);

//  5 ki 10 ar soman? athoba boro?   no "false"
console.log( 5 >= 10);

//  5 ki 10 ar soman? athoba soto?  yes boro "true"
console.log( 5 <= 10);

//  5 are 6 ki ashaman? yes "True"
console.log( 5 !== 6);

//  3ta somaon symble diye type cheke kora hoy 
 //10 are string 11 soman? no  "False"
console.log( 10 === '10');
```

